"""
TouchAnything Dataset

Reads HDF5 files generated by scripts/core/convert_to_hdf5.py and returns batches compatible with train.py:
    - 3 independent camera streams (chest=ego, left=wrist_left, right=wrist_right)
    - Ground-truth tactile pressure data (21x21 resized to tactile_size)
    - Hand joint positions (21 joints/hand, 42 total)
"""

import json
import h5py
import cv2
import torch
import numpy as np
from torch.utils.data import Dataset
from pathlib import Path
from typing import Dict, List, Tuple, Optional


class TouchAnythingDataset(Dataset):
    """
    TouchAnything dataset loader based on HDF5.

    HDF5 files are generated by scripts/core/convert_to_hdf5.py with the following layout:
        /metadata       - trajectory_id, task_name, num_frames, fps
        /timestamps     - [T] int64
        /images/{cam}_{color|depth} - [T, H, W, 3] or [T, H, W]
        /poses/{chest|left|right}_pose - [T, 7]
        /hands/{left|right}_joint_xyz  - [T, 21, 3]
        /hands/wilor_{left|right}_joint_xyz - [T, 21, 3] (optional)
        /hands/wilor_{left|right}_valid     - [T] uint8 (optional)
        /pressure/left_pressure_grid   - [T, 21, 21] float32 (baseline-corrected + interpolated)
        /pressure/right_pressure_grid  - [T, 21, 21] float32 (baseline-corrected + interpolated)
        /pressure/{left|right}_sensor_raw   - [T, 256] uint8 (raw values, kept for reference)

    Note: baseline correction and broken-sensor interpolation are already preprocessed in convert_to_hdf5.py.
    If the HDF5 file contains normalized=True, pressure values have already been normalized to [0,1]
    using the task-level vmax, so no extra per-clip normalization is needed during training.

    Output batch format (compatible with train.py / collate_fn):
        frames:       (T, 3, H, W)   chest camera (main ego view)
        poses:        (T, 42, 3)     two-hand xyz joints (left 21 + right 21)
        confidences:  (T, 42)        expanded from pose-valid masks (all ones for rokoko by default)
        views:        {ego, wrist_left, wrist_right} → (T, 3, H, W)
        pressure_maps:(T, 2, S, S)   left and right pressure maps, where S = tactile_size
        task:         str
    """

    NUM_JOINTS_PER_HAND = 21
    NUM_JOINTS = 42  # 21 left + 21 right
    GRID_SIZE = 21   # Pressure sensor grid size

    def __init__(
        self,
        data_root: str,
        mapping_left_path: str,
        mapping_right_path: str,
        clip_length: int = 16,
        frame_interval: int = 1,
        transform=None,
        split: str = 'train',
        train_ratio: float = 0.8,
        val_ratio: float = 0.1,
        image_size: Tuple[int, int] = (224, 224),
        task: str = 'tactile_prediction',
        multi_view: bool = True,
        tactile_size: int = 16,
        split_file: Optional[str] = None,
        pose_source: str = 'rokoko',
        fallback_pose_source: Optional[str] = None,
    ):
        """
        Args:
            data_root:             Root directory of the HDF5 dataset
            mapping_left_path:     Left-hand pressure mapping JSON (kept only for backward compatibility)
            mapping_right_path:    Right-hand pressure mapping JSON (kept only for backward compatibility)
            clip_length:           Number of frames per clip
            frame_interval:        Frame interval inside each clip
            transform:             Image transform (VideoTransform instance)
            split:                 'train' / 'val' / 'test'
            train_ratio:           Training split ratio (trajectory-level)
            val_ratio:             Validation split ratio
            image_size:            Target image resize size (H, W)
            task:                  'tactile_prediction' / 'pose_prediction'
            multi_view:            Whether to return a views dictionary
            tactile_size:          Output pressure-map size (SxS)
            split_file:            Path to a predefined split.json (containing train/val/test file lists);
                                   if None, fall back to ratio-based splitting
            pose_source:           Pose source: 'wilor' (recommended) or 'rokoko' (legacy)
            fallback_pose_source:  Fallback pose source when the primary source is missing (not recommended, set to None)
        """
        if pose_source not in {'rokoko', 'wilor'}:
            raise ValueError(f"Unsupported pose_source: {pose_source}. Use 'wilor' for training.")
        if fallback_pose_source not in {None, 'rokoko', 'wilor'}:
            raise ValueError(f"Unsupported fallback_pose_source: {fallback_pose_source}")

        self.data_root = Path(data_root)
        self.clip_length = clip_length
        self.frame_interval = frame_interval
        self.transform = transform
        self.split = split
        self.image_size = image_size
        self.task = task
        self.multi_view = multi_view
        self.tactile_size = tactile_size
        self.pose_source = pose_source
        self.fallback_pose_source = fallback_pose_source
        
        # Glove augmentation (only for training)
        self.glove_augmentation = None
        if hasattr(transform, 'glove_augmentation'):
            self.glove_augmentation = transform.glove_augmentation

        # Collect the HDF5 file list.
        if split_file is not None:
            with open(split_file, 'r', encoding='utf-8') as _f:
                _split_dict = json.load(_f)
            hdf5_files = [Path(p) for p in _split_dict[split]]
        else:
            all_hdf5 = sorted(self.data_root.rglob('*.hdf5'))
            if len(all_hdf5) == 0:
                raise ValueError(f"No HDF5 files found under: {self.data_root}")
            train_end = int(len(all_hdf5) * train_ratio)
            val_end = train_end + int(len(all_hdf5) * val_ratio)
            if split == 'train':
                hdf5_files = all_hdf5[:train_end]
            elif split == 'val':
                hdf5_files = all_hdf5[train_end:val_end]
            else:
                hdf5_files = all_hdf5[val_end:]

        # Build the clip index: each entry is (hdf5_path, start_frame).
        self.clips: List[Dict] = []
        for hdf5_path in hdf5_files:
            try:
                with h5py.File(hdf5_path, 'r') as f:
                    num_frames = int(f['metadata'].attrs['num_frames'])
                    task_name = str(f['metadata'].attrs['task_name'])
            except Exception as e:
                print(f"[WARN] Skipping corrupted file {hdf5_path}: {e}")
                continue

            span = (clip_length - 1) * frame_interval + 1
            if num_frames < span:
                continue

            # Sliding window with step considering frame_interval
            # Use span as step to avoid overlapping clips while respecting frame_interval
            step = span
            for start in range(0, num_frames - span + 1, step):
                self.clips.append({
                    'hdf5_path': str(hdf5_path),
                    'start_frame': start,
                    'task': task_name,
                    'num_frames': num_frames,
                })

        print(f"[TouchAnything][{split.upper()}] "
              f"{len(hdf5_files)} trajectories, {len(self.clips)} clips, "
              f"pose_source={self.pose_source}, fallback={self.fallback_pose_source}")

    # ------------------------------------------------------------------
    # Pressure-grid helper: only normalization and resize are done here.
    # ------------------------------------------------------------------

    def _grid_to_map(self, grid: np.ndarray, vmax: float):
        """
        Convert a preprocessed 21x21 grid to a (tactile_size, tactile_size) float32 map in [0, 1].
        Also returns a valid-sensor mask (1 = sensor exists, 0 = no sensor / NaN).

        Returns:
            (pressure_map, sensor_mask) — both are float32 arrays of shape (tactile_size, tactile_size)
        """
        sensor_mask = (~np.isnan(grid)).astype(np.float32)  # 1 = valid sensor location

        # Replace NaNs (locations without sensors) with 0.
        grid = np.where(np.isnan(grid), 0.0, grid)

        # Normalize.
        if vmax > 0:
            grid = np.clip(grid / vmax, 0.0, 1.0)

        # Resize 21x21 to tactile_size x tactile_size when needed.
        if self.tactile_size != self.GRID_SIZE:
            grid        = cv2.resize(grid.astype(np.float32),
                                     (self.tactile_size, self.tactile_size),
                                     interpolation=cv2.INTER_LINEAR)
            sensor_mask = cv2.resize(sensor_mask,
                                     (self.tactile_size, self.tactile_size),
                                     interpolation=cv2.INTER_NEAREST)

        return grid.astype(np.float32), sensor_mask.astype(np.float32)

    # ------------------------------------------------------------------
    # Core loading logic
    # ------------------------------------------------------------------

    def __len__(self) -> int:
        return len(self.clips)

    def _read_pose_source(
        self,
        f: h5py.File,
        source: str,
        start: int,
        end_idx: int,
        fi: int,
        T: int,
    ):
        """Read the requested pose source and return left/right xyz with valid-frame masks."""
        if source == 'wilor':
            left_key = 'hands/wilor_left_joint_xyz'
            right_key = 'hands/wilor_right_joint_xyz'
            left_valid_key = 'hands/wilor_left_valid'
            right_valid_key = 'hands/wilor_right_valid'
            required = [left_key, right_key, left_valid_key, right_valid_key]
            missing = [k for k in required if k not in f]
            if missing:
                raise KeyError(f"WiLoR datasets missing: {missing}")

            left_xyz = f[left_key][start:end_idx:fi][:T]
            right_xyz = f[right_key][start:end_idx:fi][:T]
            left_valid = f[left_valid_key][start:end_idx:fi][:T].astype(np.uint8)
            right_valid = f[right_valid_key][start:end_idx:fi][:T].astype(np.uint8)
            return left_xyz, right_xyz, left_valid, right_valid
        elif source == 'rokoko':
            # Rokoko data kept for backward compatibility but not recommended for training
            left_xyz = f['hands/left_joint_xyz'][start:end_idx:fi][:T]
            right_xyz = f['hands/right_joint_xyz'][start:end_idx:fi][:T]
            left_valid = np.ones((left_xyz.shape[0],), dtype=np.uint8)
            right_valid = np.ones((right_xyz.shape[0],), dtype=np.uint8)
            return left_xyz, right_xyz, left_valid, right_valid
        else:
            raise ValueError(f"Unknown pose source: {source}")

    def _load_pose_arrays(self, f: h5py.File, start: int, end_idx: int, fi: int, T: int):
        """Read the main pose source and fall back frame-wise when necessary."""
        try:
            left_xyz, right_xyz, left_valid, right_valid = self._read_pose_source(
                f, self.pose_source, start, end_idx, fi, T
            )
        except KeyError:
            if self.fallback_pose_source is None or self.fallback_pose_source == self.pose_source:
                raise
            left_xyz, right_xyz, left_valid, right_valid = self._read_pose_source(
                f, self.fallback_pose_source, start, end_idx, fi, T
            )
            return left_xyz, right_xyz, left_valid, right_valid

        if self.fallback_pose_source is not None and self.fallback_pose_source != self.pose_source:
            try:
                fb_left_xyz, fb_right_xyz, fb_left_valid, fb_right_valid = self._read_pose_source(
                    f, self.fallback_pose_source, start, end_idx, fi, T
                )
                missing_left = left_valid == 0
                missing_right = right_valid == 0
                if np.any(missing_left):
                    left_xyz[missing_left] = fb_left_xyz[missing_left]
                    left_valid[missing_left] = fb_left_valid[missing_left]
                if np.any(missing_right):
                    right_xyz[missing_right] = fb_right_xyz[missing_right]
                    right_valid[missing_right] = fb_right_valid[missing_right]
            except KeyError:
                pass

        return left_xyz, right_xyz, left_valid, right_valid

    def __getitem__(self, idx: int) -> Dict:
        clip_info = self.clips[idx]
        hdf5_path = clip_info['hdf5_path']
        start = clip_info['start_frame']
        T = self.clip_length
        fi = self.frame_interval

        # Frame index list.
        frame_indices = [min(start + i * fi, clip_info['num_frames'] - 1)
                         for i in range(T)]

        with h5py.File(hdf5_path, 'r') as f:
            # ---- Images ----
            # Read a contiguous block first and then subsample (faster than fancy indexing).
            end_idx = frame_indices[-1] + 1
            chest_color = f['images/chest_color'][start:end_idx:fi][:T]  # (T, H, W, 3)
            left_color  = f['images/left_color'][start:end_idx:fi][:T]
            right_color = f['images/right_color'][start:end_idx:fi][:T]

            # ---- Hand joints (WiLoR recommended for training) ----
            left_xyz, right_xyz, left_valid, right_valid = self._load_pose_arrays(
                f, start, end_idx, fi, T
            )

            # ---- Preprocessed pressure grids (baseline correction + interpolation already applied) ----
            left_grids  = f['pressure/left_pressure_grid'][start:end_idx:fi][:T]   # (T, 21, 21)
            right_grids = f['pressure/right_pressure_grid'][start:end_idx:fi][:T]
            already_normalized = bool(f['pressure'].attrs.get('normalized', False))
            
            # ---- Load glove masks for augmentation (if available) ----
            glove_masks = None
            if self.glove_augmentation is not None:
                glove_masks = self._load_glove_masks(f, frame_indices)

        # ---- Apply glove augmentation (before transform) ----
        if glove_masks is not None and self.glove_augmentation is not None:
            chest_color = self.glove_augmentation(chest_color, glove_masks)
        
        # ---- Image transform ----
        chest_tensor = self._apply_transform(chest_color)   # (T, 3, H, W)
        left_tensor  = self._apply_transform(left_color)
        right_tensor = self._apply_transform(right_color)

        # ---- Joint positions ----
        poses = np.concatenate([left_xyz, right_xyz], axis=1)   # (T, 42, 3)
        
        # Mark invalid poses with a special value BEFORE clipping
        # This allows the model to distinguish invalid poses from valid ones
        INVALID_POSE_VALUE = -10.0
        for t in range(T):
            if left_valid[t] == 0:
                poses[t, :self.NUM_JOINTS_PER_HAND, :] = INVALID_POSE_VALUE
            if right_valid[t] == 0:
                poses[t, self.NUM_JOINTS_PER_HAND:, :] = INVALID_POSE_VALUE
        
        # Clip abnormal pose coordinates to avoid extreme values from failed WiLoR predictions.
        # x/y: horizontal and vertical offsets, clipped to [-10, 10] meters (expanded to include invalid marker)
        # z: depth value, typically 10-50 meters, clipped to [-10, 100] meters (expanded to include invalid marker)
        poses[:, :, 0] = np.clip(poses[:, :, 0], -10.0, 10.0)  # x
        poses[:, :, 1] = np.clip(poses[:, :, 1], -10.0, 10.0)  # y
        poses[:, :, 2] = np.clip(poses[:, :, 2], -10.0, 100.0)  # z
        
        poses_tensor = torch.from_numpy(poses.astype(np.float32))
        left_conf = np.repeat(left_valid[:, None], self.NUM_JOINTS_PER_HAND, axis=1)
        right_conf = np.repeat(right_valid[:, None], self.NUM_JOINTS_PER_HAND, axis=1)
        confidences_np = np.concatenate([left_conf, right_conf], axis=1).astype(np.float32)
        confidences = torch.from_numpy(confidences_np)

        result = {
            'frames':      chest_tensor,   # backward compatibility: ego view
            'poses':       poses_tensor,   # (T, 42, 3)
            'confidences': confidences,    # (T, 42)
            'task':        clip_info['task'],
        }

        if self.multi_view:
            result['views'] = {
                'ego':         chest_tensor,
                'wrist_left':  left_tensor,
                'wrist_right': right_tensor,
            }

        # ---- Pressure maps (only for the tactile_prediction task) ----
        if self.task == 'tactile_prediction':
            if already_normalized:
                vmax = 1.0
            else:
                # Backward compatibility for older HDF5 files: dynamic clip-level normalization
                vmax = max(np.nanmax(left_grids), np.nanmax(right_grids), 1.0)

            pressure_maps = np.zeros(
                (T, 2, self.tactile_size, self.tactile_size), dtype=np.float32)
            sensor_masks  = np.zeros(
                (T, 2, self.tactile_size, self.tactile_size), dtype=np.float32)
            for t in range(T):
                pressure_maps[t, 0], sensor_masks[t, 0] = self._grid_to_map(left_grids[t],  vmax)
                pressure_maps[t, 1], sensor_masks[t, 1] = self._grid_to_map(right_grids[t], vmax)

            result['pressure_maps'] = torch.from_numpy(pressure_maps)  # (T, 2, S, S)
            result['sensor_mask']   = torch.from_numpy(sensor_masks)   # (T, 2, S, S)

        return result

    def _apply_transform(self, frames_np: np.ndarray) -> torch.Tensor:
        """
        (T, H, W, 3) uint8 → (T, 3, H, W) float tensor

        If a transform (VideoTransform) is provided, use it.
        Otherwise, apply minimal preprocessing: resize + to tensor + ImageNet normalization.
        """
        h_t, w_t = self.image_size

        # Resize (VideoTransform also resizes internally, so avoid doing it twice).
        if self.transform is None:
            T_len = frames_np.shape[0]
            resized = np.stack([
                cv2.resize(frames_np[t], (w_t, h_t))
                for t in range(T_len)
            ])  # (T, H, W, 3)
            tensor = torch.from_numpy(resized).float().permute(0, 3, 1, 2) / 255.0
            # ImageNet normalization
            mean = torch.tensor([0.485, 0.456, 0.406]).view(1, 3, 1, 1)
            std  = torch.tensor([0.229, 0.224, 0.225]).view(1, 3, 1, 1)
            return (tensor - mean) / std
        else:
            return self.transform(frames_np)  # (T, 3, H, W)
    
    def _load_glove_masks(self, hdf5_file, frame_indices: list) -> Optional[np.ndarray]:
        """
        Load glove masks from HDF5 file for augmentation.
        
        Args:
            hdf5_file: Open h5py.File object
            frame_indices: List of frame indices to load
        
        Returns:
            masks: (T, H, W) numpy array, binary mask (0 or 255), uint8
                   Returns None if no masks available
        """
        # Check if masks exist in HDF5
        if 'masks' not in hdf5_file:
            return None
        
        masks_group = hdf5_file['masks']
        if 'glove_masks' not in masks_group:
            return None
        
        glove_masks_group = masks_group['glove_masks']
        
        # Check if valid_frames exists (use try-except to avoid FutureWarning)
        try:
            if 'valid_frames' not in glove_masks_group.keys():
                return None
        except:
            return None
        
        valid_frames = glove_masks_group['valid_frames'][:]
        
        # If no valid frames, return None
        if not np.any(valid_frames):
            return None
        
        # Load masks for requested frames
        masks_dataset = glove_masks_group['masks']
        T = len(frame_indices)
        H, W = masks_dataset.shape[1], masks_dataset.shape[2]
        
        masks = np.zeros((T, H, W), dtype=np.uint8)
        
        for i, frame_idx in enumerate(frame_indices):
            if frame_idx < len(valid_frames) and valid_frames[frame_idx]:
                masks[i] = masks_dataset[frame_idx]
        
        return masks
